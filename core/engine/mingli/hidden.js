/**
 * 地支藏干表（本气/中气/余气）
 */
export const HIDDEN = {
  "子": [["癸","本"]],
  "丑": [["己","本"],["癸","中"],["辛","余"]],
  "寅": [["甲","本"],["丙","中"],["戊","余"]],
  "卯": [["乙","本"]],
  "辰": [["戊","本"],["乙","中"],["癸","余"]],
  "巳": [["丙","本"],["戊","中"],["庚","余"]],
  "午": [["丁","本"],["己","余"]],
  "未": [["己","本"],["丁","中"],["乙","余"]],
  "申": [["庚","本"],["壬","中"],["戊","余"]],
  "酉": [["辛","本"]],
  "戌": [["戊","本"],["辛","中"],["丁","余"]],
  "亥": [["壬","本"],["甲","中"]]
};

/**
 * 获取地支的藏干（带标签）
 */
export function hiddenTaggedOf(branch) {
  return HIDDEN[branch] || [];
}

/**
 * 获取地支的藏干（纯天干）
 */
export function hiddenPlainOf(branch) {
  return (HIDDEN[branch] || []).map(x => x[0]);
}
